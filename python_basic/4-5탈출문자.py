# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")   # \n 줄바꿈

# \" \' : 문장 내에서 따옴표
# 저는 '조준호' 입니다.
print("저는 'name' 입니다")

# 저는 "조준호" 입니다.
print('저는 "name" 입니다')
print("저는 \"name\" 입니다")

# \\ : 문장 내에서 \
print("C:\\Users\\username\\Desktop\\pythonWorkspace> ")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\b Apple")

# \t : 탭
print("Red\tApple")